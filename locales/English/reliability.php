<p class="intro">
The reliable rating is an easy calculation that represents how reliable you enter your commands and how reliable you play
your games till the end.
</p>

<div class="hr" ></div>

<p class="intro">
Your rating is dependend on 2 important factors. How many phases you missed to enter orders in comparison to your total phases played, 
and how many games your country went into CivilDisorder, because you didn't even check the gamepage for 2 turns in a row.<br>
The first part is 100 minus phases missed / phases played * 200, not to be lower than 0.<br>
<b>Example</b>: If a user misses 5% of their games, rating would be 90, 15% would be 70, etc<br>
From this rating we substract 10% for each game you left bevore the end.
</p>

<p class="intro">
The penality for the "Left" games seems a bit harsh, but many games get totally screwed if a player does not play the game till the end.
Most of the time some countries gain really big unearned advantages. But you can even out your lost reliability by taking an open spot 
from a game another player left.
</p>

<p class="intro">
When someone creates a game they can select a minimum rating for the people able to enter their games, and if you rating is too 
low you might not be able to join all the games as you like.<br>
Also for each 10% of reliability you can join 1 game. If your reliability is 90% or better you can join as many games as you want.
</p>

